package Modelos;


public class Datos {

String Valor;
int Clave;

    public Datos(String Valor, int Clave) {
        this.Valor = Valor;
        this.Clave = Clave;
    }

    public String getValor() {
        return Valor;
    }

    public void setValor(String Valor) {
        this.Valor = Valor;
    }

    public int getClave() {
        return Clave;
    }

    public void setClave(int Clave) {
        this.Clave = Clave;
    }



}
